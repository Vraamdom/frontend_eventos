    const table_part = document.getElementById('tblPart');
    const pagination_part = document.getElementById('pagination_part');
    const prevPageButton_part = document.getElementById('prevPage_part');
    const nextPageButton_part = document.getElementById('nextPage_part');
    const currentPageSpan_part = document.getElementById('currentPage_part');
    let pagina_principal = 1;
    const itemsPerPage_part = 5; // Cambia esto para ajustar la cantidad de filas por página
  
    function showdatos(page) {
      const startIndex = (page - 1) * itemsPerPage_part;
      const endIndex = startIndex + itemsPerPage_part;
      const paginateddatos = datos.slice(startIndex, endIndex);
  
      table_part.tBodies[0].innerHTML = '';
  
      for (const item of paginateddatos) {
        const row = table_part.tBodies[0].insertRow();
        row.insertCell(0).textContent = item.id;
        row.insertCell(1).textContent = item.tipo_participante;
        row.insertCell(2).textContent = item.edad_minima;
        row.insertCell(3).textContent = item.edad_maxima;
        row.insertCell(4).textContent = item.genero_biologico;
        row.insertCell(5).textContent = item.nro_invitados_por_tipo;

        const opcionesCell = row.insertCell(6);
        opcionesCell.className ='Opciones';
  
        opcionesCell.innerHTML = `
        <td class="Opciones">
          <div>
              <a href="editar_participante"><img
                      src="../images/editar.png" alt="icon"
                      class="opc_edit"></a>
          </div>
          <div>
              <img src="../images/borrar.png" alt="icon"
                  class="opc_delete" onclick="mostrarAlerta()">
          </div>
        </td>
          `;
      }
     
  
      pagina_principal = page;
      currentPageSpan_part.textContent = pagina_principal;
      prevPageButton_part.disabled = pagina_principal === 1;
      nextPageButton_part.disabled = pagina_principal === Math.ceil(datos.length / itemsPerPage_part);
    }
  
    prevPageButton_part.addEventListener('click', () => {
      if (pagina_principal > 1) {
        showdatos(pagina_principal - 1);
      }
    });
  
    nextPageButton_part.addEventListener('click', () => {
      if (pagina_principal < Math.ceil(datos.length / itemsPerPage_part)) {
        showdatos(pagina_principal + 1);
      }
    });
  
    showdatos(pagina_principal); // Mostrar la primera página al cargar la página
  
  
